# *********************    VARIÁVEIS    *********************
#
# Variáveis são um conceito fundamental em programação 
# e são usadas para armazenar e manipular dados em um 
# programa. Elas são como recipientes ou rótulos que 
# você pode usar para armazenar informações e referenciá-las 
# posteriormente
#
# Aqui está uma explicação mais detalhada sobre variáveis:
#
# 1 - Armazenamento de Dados: Variáveis são usadas para armazenar 
# dados. Os dados podem ser de diferentes tipos, como números, texto, 
# booleanos e estruturas de dados mais complexas.
#
# 2 - Nomeação: As variáveis têm nomes que você escolhe, que devem seguir 
# regras específicas de nomenclatura na linguagem de programação 
# em que você está trabalhando. Os nomes das variáveis são usados 
# para referenciar os dados armazenados.
#
# 3 - Atribuição: Para criar uma variável e atribuir um valor a ela, 
# você usa um operador de atribuição
#
# 4 - Tipos de Dados: As variáveis têm tipos de dados associados. Isso 
# significa que elas podem conter valores de um tipo específico, como 
# inteiros, strings, listas, etc. 
#
# 5 - Reatribuição:  Isso significa atualizar o conteúdo da variável 
# ao longo do tempo.
#
# 6 - Uso de Variáveis: Variáveis são usadas para armazenar resultados 
# intermediários, facilitar o controle de fluxo em programas (usando 
# condicionais e loops), melhorar a legibilidade do código, evitar 
# duplicação de valores e muito mais.
#
# 7 - Escopo: As variáveis têm escopo, o que significa que elas só são 
# visíveis e acessíveis em partes específicas do código. Isso ajuda a 
# evitar conflitos de nomes de variáveis em diferentes partes do programa.
#
# 8 - Nomes Descritivos: É uma boa prática escolher nomes de variáveis 
# que descrevam claramente o propósito da variável, tornando o código 
# mais legível.