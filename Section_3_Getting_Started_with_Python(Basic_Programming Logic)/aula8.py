# ***********   FUNÇÃO input()   ***********
#
# Em Python, a função input() é usada para receber 
# entrada de dados do usuário através do teclado. 
# Ela permite que você solicite que o usuário insira 
# ou valores, que são lidos como uma sequência de 
# caracteres (strings). O formato básico de uso da 
# função input() é o seguinte:
variavel = input("Mensagem para o usuário: ")
#
# 1 - A função input() exibe a mensagem entre 
# parênteses (no exemplo acima, "Mensagem para 
# o usuário:") no console para instruir o 
# usuário sobre o que deve inserir.
#
# 2 - O programa aguarda até que o usuário 
# insira uma linha de texto no teclado, 
# seguida pela tecla "Enter" (ou "Return").
#
# 3 - Quando o usuário pressiona "Enter", 
# o que foi digitado é capturado como uma 
# string e atribuído à variável.
#
# 4 - Caso seja digitado números, eles 
# ainda serão tratados como uma string, e 
# se necessário realizar operações matemáticas, 
# é preciso convertê-los para tipos numéricos, 
# como int ou float, usando funções como int() 
# ou float().


